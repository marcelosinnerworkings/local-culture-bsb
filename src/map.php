    <p>You may want to visit Brasilia taking into account the following means of transportation:</p>
    <br>
      <ul class="w3-ul">
        <li><strong>Plane:</strong> 
          There is only one airport for passengers in Brasilia: <a href="https://www.bsb.aero/" target="_blank">Presidente Juscelino Kubitschek International Airport (BSB)</a>.
          Airport shops in Brazil are pricey. Should you want to buy more affordable souvenirs or mementoes, it is recommended to consider shops and stores far from tourism hotspots.
          Private air taxi (jets and helicopters) might be available for national trips, but this is not a very cheap service in Brazil.
        </li>
        <li><strong>Bus:</strong> 
          There is a specific terminal for inter-state bus travel: <a href="https://www.terminalrodoviariobrasilia.com.br/" target="_blank">Brasilia Inter-State Bus Terminal</a>.
          Inter-state bus services are usually punctual and dependable. Booking seats in advance is recommended.
          Likewise, bigger groups of people may consider hiring or renting private buses.
          Nonetheless, regular bus lines that cover urban areas in Brasilia (or DF) are not very reliable in terms of quality and time. 
          Some neighborhoods do not have access to urban buses at all.
          If you have scheduled a local appointment in which punctuality is important, you are advised to consider other transportation options.
        </li>
        <li><strong>Train:</strong> 
          There are no inter-state train services for passengers in Brasilia.
          However, there is an urban subway which covers the regions of Asa Sul (Plano Piloto), Guará, Águas Claras, Taguatinga, Ceilândia and Samambaia.
          The subway is usually trustworthy, even though waiting times between trains might take a while (less than 15-30 minutes in most cases).
          The subway tends to become very crowded during peak hours in workdays. 
        </li>
        <li><strong>Car:</strong> 
          It is suggested that car rental services are evaluated beforehand. 
          Unless you trust or know local professionals, look for the biggest car rental and transfer providers.
          Avoid accepting local car offers and transfers at the airport as they tend to be riskier or more expensive.
          In case you prefer smaller or local businesses, ask for guidance from the accommodation staff or your host. 
          Uber and other similar apps are available, as well.
        </li>
        <li><strong>Bicycle:</strong>
          Most of the city center is plain. 
          Bike rides are relatively easy and hassle-free in the central region (Plano Piloto), and there are several bike paths along the way.
          It is possible to either hire or rent bikes in different spots of the city.
          Use sunscreen and comfortable clothes, because Brasilia is hot and dry most of the times. 
          The rainy season occurs between October and April, although there can be sparse drizzles in other periods of the year. 
          Please note that it is not always safe to ride bikes during the night.
          Also, avoid riding a bike in streets and roads. Reckless driving is unfortunately frequent in Brasilia.

        </li>
      </ul>
